        var bacButtonImage = UIImage(systemName: "arrow.backward", withConfiguration: UIImage.SymbolConfiguration(pointSize: 20.0, weight: .bold))
      
        bacButtonImage = bacButtonImage?.withAlignmentRectInsets(UIEdgeInsets(top: 0, left: -10, bottom: 0, right: 0))
        navBarAppearance.setBackIndicatorImage(bacButtonImage, transitionMaskImage: bacButtonImage)
        
        UINavigationBar.appearance().tintColor = .black
        UINavigationBar.appearance().standardAppearance = navBarAppearance
        UINavigationBar.appearance().compactAppearance = navBarAppearance
        UINavigationBar.appearance().scrollEdgeAppearance = navBarAppearance
        
        let tabbar = UITabBarAppearance()
        tabbar.configureWithOpaqueBackground()
        
        UITabBar.appearance().tintColor = UIColor(named: "NavigationBarTitle")
        UITabBar.appearance().standardAppearance = tabbar

        guard let _ = (scene as? UIWindowScene) else { return }
    }

